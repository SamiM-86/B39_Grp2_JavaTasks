package kubilay;

public class Week3_SwapNumbers {

    public static void main(String[] args) {

        int num1 = 3;
        int num2 = 7;

        num1 = num1 + num2;

        num2 = num1 - num2;

        num1 = num1 - num2;

        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);

    }
}
